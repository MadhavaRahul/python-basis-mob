a=1
b=5
c=4
#formula for two roots
x1=(-b+(b**2-4*a*c)**0.5)/2*a
x2=(-b-(b**2-4*a*c)**0.5)/2*a

print(f'x1= {x1}') #1st root
print(f'x2= {x2}') #2st root

""" sum 
        of 
            the 
                roots """
print(f'x1+x2= {x1+x2}')
""" product 
        of 
            the 
                roots """
print(f'x1*= {x1*x2}')
